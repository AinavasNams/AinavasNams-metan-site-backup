#!/usr/bin/env python3
"""Add partneriem page translations to i18n.ts"""

FILE = "/root/metan-site/lib/i18n.ts"

with open(FILE, 'r') as f:
    content = f.read()

with open(FILE + '.backup-partneriem-page', 'w') as f:
    f.write(content)

lines = content.split('\n')

# Find insertion points (before closing `  },` of each language block)
lv_end = ru_end = lt_end = en_end = None

for idx, line in enumerate(lines):
    if '🇷🇺 РУССКИЙ' in line and lv_end is None:
        for j in range(idx-1, idx-5, -1):
            if lines[j].rstrip() == '  },':
                lv_end = j; break
    if '🇱🇹 LIETUVIŲ' in line and ru_end is None:
        for j in range(idx-1, idx-5, -1):
            if lines[j].rstrip() == '  },':
                ru_end = j; break
    if '🇬🇧 ENGLISH' in line and lt_end is None:
        for j in range(idx-1, idx-5, -1):
            if lines[j].rstrip() == '  },':
                lt_end = j; break

for idx, line in enumerate(lines):
    if line.rstrip() == '};':
        for j in range(idx-1, idx-5, -1):
            if lines[j].rstrip() == '  },':
                en_end = j; break
        break

print(f"LV ends at line {lv_end+1}, RU at {ru_end+1}, LT at {lt_end+1}, EN at {en_end+1}")

if not all([lv_end, ru_end, lt_end, en_end]):
    print("ERROR: Could not find all insertion points!")
    exit(1)

# Check if partneriemPage already exists
if 'partneriemPage:' in content:
    print("partneriemPage already exists in i18n.ts, skipping.")
    exit(0)

